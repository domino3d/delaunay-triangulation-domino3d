    private static class filter extends FileFilter {
        public boolean accept(File file) {
            if (file.isDirectory()) {
                return true;
            }
            String filename = file.getName();
            if (filename.endsWith(".obj") || filename.endsWith(".obj")) {
                return true;
            }else return false;
        }
        public String getDescription() {
            return "*.obj";
        }
    }
    
    
