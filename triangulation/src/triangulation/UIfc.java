private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      JFileChooser c = new JFileChooser();
      // Demonstrate "Open" dialog:
      
      c.setFileFilter(new filter());
      int rVal = c.showOpenDialog(UIfc.this);
      
      if (rVal == JFileChooser.APPROVE_OPTION) {
        jLabel10.setText(c.getSelectedFile().getName());
        jLabel9.setText(c.getCurrentDirectory().toString());
        file = c.getSelectedFile().getAbsolutePath();
        jButton1.setText("Ok");
      }
      if (rVal == JFileChooser.CANCEL_OPTION) {
        jLabel10.setText("Stlacili ste Cancel");
        jLabel9.setText("");
        jButton1.setText("Koniec");
      }
      
}//GEN-LAST:event_jButton2ActionPerformed

